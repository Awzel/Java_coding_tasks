package Week02_Tasks;

public class swapNumbers_Agil {

    public static void main(String[] args) {
        // swap two variables' values without a third variable

        int a = 3;
        int b = 4;
       // a  = 7
        a = a + b;
        System.out.println("a = " + a);
        // b = 7- 4 = 3
        b = a - b;
        System.out.println("b = " + b);
        // a = 7 - 3 = 4
        a = a - b;
        System.out.println("a = " + a);


    }
}
